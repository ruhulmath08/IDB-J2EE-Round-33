package listExampleProject3;

public class Thana {
    private String districtName;
    private long districtZepCode;

    public Thana(String districtName, long districtZepCode) {
        this.districtName = districtName;
        this.districtZepCode = districtZepCode;
    }

    public String getDistrictName() {
        return districtName;
    }

    public long getDistrictZepCode() {
        return districtZepCode;
    }
    
    
}
